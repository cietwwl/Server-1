package com.rwbase.dao.arena.pojo;

public class ArenaCost {

	private int times;
	private int resetCost;
	private int buyTimesCost;

	public int getTimes() {
		return times;
	}

	public void setTimes(int times) {
		this.times = times;
	}

	public int getResetCost() {
		return resetCost;
	}

	public void setResetCost(int resetCost) {
		this.resetCost = resetCost;
	}

	public int getBuyTimesCost() {
		return buyTimesCost;
	}

	public void setBuyTimesCost(int buyTimesCost) {
		this.buyTimesCost = buyTimesCost;
	}

}
