package com.rwbase.dao.worship.pojo;

public class CfgWorshipRandomReward {
	private int scheme;
	private String weightGroup;
	private int itemID;
	private int weight;
	private int max;
	public int getScheme() {
		return scheme;
	}
	public void setScheme(int scheme) {
		this.scheme = scheme;
	}
	public String getWeightGroup() {
		return weightGroup;
	}
	public void setWeightGroup(String weightGroup) {
		this.weightGroup = weightGroup;
	}
	public int getItemID() {
		return itemID;
	}
	public void setItemID(int itemID) {
		this.itemID = itemID;
	}
	public int getWeight() {
		return weight;
	}
	public void setWeight(int weight) {
		this.weight = weight;
	}
	public int getMax() {
		return max;
	}
	public void setMax(int max) {
		this.max = max;
	}
}
