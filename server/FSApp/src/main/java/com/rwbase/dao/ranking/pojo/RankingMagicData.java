package com.rwbase.dao.ranking.pojo;

public class RankingMagicData {
	private String magicImage;
	private int magicQuality;
	private int magicAttackType;

	public String getMagicImage() {
		return magicImage;
	}

	public void setMagicImage(String magicImage) {
		this.magicImage = magicImage;
	}

	public int getMagicQuality() {
		return magicQuality;
	}

	public void setMagicQuality(int magicQuality) {
		this.magicQuality = magicQuality;
	}

	public int getMagicAttackType() {
		return magicAttackType;
	}

	public void setMagicAttackType(int magicAttackType) {
		this.magicAttackType = magicAttackType;
	}
}
