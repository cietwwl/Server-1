package com.rwbase.dao.role.pojo;

public class RoleQualityColorCfg {
	private int quality;
	private String colorName;
	private String color;
	private String qualityBG;
	private String mainRoleInBattleBG;
	private int activeSkill;
	public int getQuality() {
		return quality;
	}
	public void setQuality(int quality) {
		this.quality = quality;
	}
	public String getColorName() {
		return colorName;
	}
	public void setColorName(String colorName) {
		this.colorName = colorName;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public String getQualityBG() {
		return qualityBG;
	}
	public void setQualityBG(String qualityBG) {
		this.qualityBG = qualityBG;
	}
	public String getMainRoleInBattleBG() {
		return mainRoleInBattleBG;
	}
	public void setMainRoleInBattleBG(String mainRoleInBattleBG) {
		this.mainRoleInBattleBG = mainRoleInBattleBG;
	}
	public int getActiveSkill() {
		return activeSkill;
	}
	public void setActiveSkill(int activeSkill) {
		this.activeSkill = activeSkill;
	}
}
