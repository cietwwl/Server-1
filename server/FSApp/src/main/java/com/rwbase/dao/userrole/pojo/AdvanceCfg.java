package com.rwbase.dao.userrole.pojo;

public class AdvanceCfg {

	private String playerId;
	private String nextPlayerId;
	private int advanceLevel;
	private String skillId;
	private String attrArr;
	public String getPlayerId() {
		return playerId;
	}
	public void setPlayerId(String playerId) {
		this.playerId = playerId;
	}
	public String getNextPlayerId() {
		return nextPlayerId;
	}
	public void setNextPlayerId(String nextPlayerId) {
		this.nextPlayerId = nextPlayerId;
	}
	public int getAdvanceLevel() {
		return advanceLevel;
	}
	public void setAdvanceLevel(int advanceLevel) {
		this.advanceLevel = advanceLevel;
	}
	public String getSkillId() {
		return skillId;
	}
	public void setSkillId(String skillId) {
		this.skillId = skillId;
	}
	public String getAttrArr() {
		return attrArr;
	}
	public void setAttrArr(String attrArr) {
		this.attrArr = attrArr;
	}

}
