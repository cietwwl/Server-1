package com.config.copyCfg;

public class CopyLevelNode {
	
	private int before = 0;
	
	private int id = 0;
	
	private int next = 0;
	
	public int getBefore() {
		return before;
	}
	
	public void setBefore(int before) {
		this.before = before;
	}
	
	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	public int getNext() {
		return next;
	}
	
	public void setNext(int next) {
		this.next = next;
	}
}
