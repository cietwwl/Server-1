package com.rwbase.dao.peakArena.pojo;

public class PeakArenaScoreCfg {

	private int scoreLv;
    private String range;
    private int score;
    private int maxScore;
    private String name;
    private String icon;
    private String kuang;
    
	public int getScoreLv() {
		return scoreLv;
	}
	public void setScoreLv(int scoreLv) {
		this.scoreLv = scoreLv;
	}
	public String getRange() {
		return range;
	}
	public void setRange(String range) {
		this.range = range;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	public int getMaxScore() {
		return maxScore;
	}
	public void setMaxScore(int maxScore) {
		this.maxScore = maxScore;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getIcon() {
		return icon;
	}
	public void setIcon(String icon) {
		this.icon = icon;
	}
	public String getKuang() {
		return kuang;
	}
	public void setKuang(String kuang) {
		this.kuang = kuang;
	}
	
}
